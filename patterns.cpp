#include <iostream>
using namespace std;

int main(){

    //Square Pattern

    for(int i=0;i<5;i++){                        // * * * *
        for(int j=0;j<5;j++){                    // * * * *
            cout<<"*"<<' ';                      // * * * *
        }                                        // * * * *
        cout<<endl;
    }


    for(int i=0;i<4;i++){                        // 1 2 3 4
        for(int j=1;j<=4;j++){                   // 1 2 3 4
            cout<<j<<' ';                        // 1 2 3 4
        }                                        // 1 2 3 4
        cout<<endl;
    }


    for(int i=0;i<4;i++){  
        char ch = 'A';                           // A B C D
        for(int j=1;j<=4;j++){                   // A B C D
            cout<<ch<<' ';                       // A B C D
            ch++;                                // A B C D
        }                                        
        cout<<endl;
    }


    int num = 1;
    for(int i=0;i<4;i++){                         // 1 2 3 4
        for(int j=1;j<=4;j++){                    // 5 6 7 8     
            cout<<num<<' ';                       // 9 10 11 12
            num++;                                // 13 14 15 16
        }                                        
        cout<<endl;
    }

//-----------------------------------------------------------------------------------------------------

    //Triangle Pattern

    for(int i=0;i<4;i++){                         // *
        for(int j=0;j<=i;j++){                    // * *  
            cout<<"*"<<' ';                       // * * *
        }                                         // * * * *
        cout<<endl;
    }


    for(int i=1;i<=4;i++){                        // 1 
        for(int j=1;j<=i;j++){                    // 2 2     
            cout<<i<<' ';                         // 3 3 3 
        }                                         // 4 4 4 4 
        cout<<endl;
    }


    for(int i=1;i<=4;i++){                       // 1 
        for(int j=1;j<=i;j++){                   // 1 2     
            cout<<j<<' ';                        // 1 2 3 
        }                                        // 1 2 3 4 
        cout<<endl;
    }

//-----------------------------------------------------------------------------------------------------

    //Reverse Triangle Pattern 

    for(int i=1;i<=4;i++){                       // 1 
        for(int j=i;j>0;j--){                    // 2 1     
            cout<<j<<' ';                        // 3 2 1
        }                                        // 4 3 2 1
        cout<<endl;
    }

//-----------------------------------------------------------------------------------------------------

    //Floyd's Triangle Pattern 

    int num =1;
    for(int i=1;i<=4;i++){                       // 1 
        for(int j=1;j<=i;j++){                   // 2 3     
            cout<<num<<' ';                      // 4 5 6 
            num++;                               // 7 8 9 10
        }                                        
        cout<<endl;
    }


//-----------------------------------------------------------------------------------------------------

    //Inverted Triangle Pattern 

    for(int i = 0;i<4;i++){
        for(int j =0;j<i;j++){                   // 1 1 1 1
            cout<<' ';                           //   2 2 2
        }                                        //     3 3
                                                 //       4
        for(int j=0;j<4-i;j++){
            cout<<i+1;
        }
        cout<<endl;
    }


//-----------------------------------------------------------------------------------------------------

    //Pyramid Pattern 

    for(int i = 0;i<4;i++){
        for(int j=0; j<4-i; j++){               //       1
            cout<<" ";                          //     1 2 1
        }                                       //   1 2 3 2 1
                                                // 1 2 3 4 3 2 1

        for(int j=0;j<i+1;j++){
            cout<<j+1;
        }
        
        for(int j=i;j>0;j--){
            cout<<j;
        }
        
        cout<<endl;
    }


//-----------------------------------------------------------------------------------------------------

    //Pattern



//-----------------------------------------------------------------------------------------------------

    //Pattern




    return 0;
}